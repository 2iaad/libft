#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	i;

	i = 0;
	if (!s || !f)
		return ;
	if (s != NULL)
	{
		while (s[i] && f)
		{
			f(i, &s[i]);
			i++;
		}
	}
}
